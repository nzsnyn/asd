#include <iostream>
using namespace std;

// The while loop will stop when the variable (i) is no longer less than 5.
// The variable (i) is increased by 1 each time the code in the loop has been executed.
int main()
{
    int i = 0;
    while (i < 5) {
      cout << i << "\n";
      i++;
    }
    return 0;
}

